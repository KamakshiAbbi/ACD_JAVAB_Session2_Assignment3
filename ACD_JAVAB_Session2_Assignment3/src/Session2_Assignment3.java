public class Session2_Assignment3 {
	public static void main(String[] args) {
		for (int i = 1; i < 6; i++) {
			for (int j = 1; j < i; j++) {
				System.out.print(j);
			}
			System.out.println(i);	
		}
		for (int i = 4; i >= 1; i--) {
			for (int j = 1; j < i; j++) {
				System.out.print(j);
		}
		System.out.println(i);	
		}
	}
}
